public class Modifier
{
    private Move move;
    private Character source;

    public Move Move
    {
        get
        {
            return move;
        }

        set
        {
            move = value;
        }
    }

    public Character Source
    {
        get
        {
            return source;
        }

        set
        {
            source = value;
        }
    }
}
